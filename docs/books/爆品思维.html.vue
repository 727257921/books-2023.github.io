<p>◆ 第一章 成为爆品的五大基础</p>
<blockquote>
<blockquote>
<p>第一章　成为爆品的五大基础</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>01　每一百款产品，难得一个爆品。爆品对特定人群具有与生俱来的诱惑力，颜值很高，用户消费前、中、后愿意分享，且大多是基于微创新，基于强需求，基于价值大于价格这三个维度。然而，符合爆品的基础仅仅是爆品历练的开始，爆品之于互联网如美酒之于深巷，应该创造爆点，制造话题，引发自传播，继而带动布局完毕的电商平台热销……</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>创业者失败的原因各有各的不同——有的是产品不行、有的是价格离谱、有的是模式不行、有的是营销乏力等。不一而足，而1％成功的原因大多相似——都找到了一条打造爆品的路径……</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>普通产品是先天的——企业生产什么再去卖什么；爆品是后天的——消费者需要什么我们卖什么。回归消费需求，理解用户思维，创造出“未来产品”，这是想干爆品的第一步……</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>爆品多为基于日常强需产品而迭代的微创新产品，而微创新主要集中在两个层面。其一是基于产品缺陷而改进了用户体验；其二是基于竞争对手的矮、胖、矬而改进了颜值。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>爆品首要是强需，强需自带流量；其次是微创新，微创新抓人眼球；最后才是价格，价值必须大于价格。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在传统营销时代，“企业说给用户听”正逐步被“用户参与进来”替代；企业通过多产品组合的产品线满足不同客户需求的“货架战略”逐步被“爆品战略“迎头赶上……</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>“爆品战略”代表着以用户思维为导向的研发、生产、营销，代表着一款产品找到了用户的需求，实现了用户的参与，代表着精确细分和高度聚焦。唯有不断打磨后的产品才能在繁杂的信息世界里崭露头角，而经过“打磨”后的产品才有爆品的潜质。</p>
</blockquote>
</blockquote>
<p>◆ 1 看需求——小众才有强需求</p>
<blockquote>
<blockquote>
<p>1　看需求——小众才有强需求</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>大众产品就是日常生活消费品，如柴米油盐、家用电器、衣帽鞋袜、牙膏牙刷等，这些都是普通老百姓日常消费的商品。创业者如果选择大众产品来介入，就意味着进入到一个竞争红海，也意味着进入了低价肉搏。关键是这些产品大多已被成熟品牌垄断，消费者的心智资源已被抢占，虎口夺食的难度可想而知。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>成为爆品的第一大基础是人群对产品需求，是大众还是小众？是强需求还是弱需求？</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（1）何为小众</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>往往是精准定位某一类人群的产品，比如清火宝、自拍杆、免熨衬衫、汽车护腰宝、车载净化器、核桃油等，小众产品要和强需求结合，这样既可以避免与大众强需求的产品正面对抗，又可以在小众人群中树立专业的地位，相比较而言，此类产品突围的概率会更高一些。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>聚焦某一个族群 ，比如左撇子人群、越野车一族、电子发烧友、出租车司机、漫画爱好者、广场舞大妈、爱跑步者、微博客、天秤座女生等。这类人群有着明显的族群标签，容易与大众人群区分开来，而且他们分布在各个兴趣社群里，找到一个就找到一窝。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>聚焦某个年龄段 ，比如小学生、高考落榜生、85后上班族、宝宝妈、70后大叔、退休教师等。这里说的某个年龄段，并非年龄差距越大越好，最佳年龄段是年龄差距控制在5岁左右。比如说某款学习文具针对小学生的妈妈。那么80％以上小学生的妈妈年龄在32～38岁。这个年龄段的女性几乎是同一个年代的人，基本没有代沟，有着共同的价值观和兴趣爱好，在传播的过程中我们的广告才能更聚焦。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（2）何为强需求</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>无需教育的强需求</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>穿衣、吃饭、坐公交，这样的需求不需要教育；用电脑、玩手机、看电视，这样的需求已被引导。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>一个颠覆性创新的产品，需要教育市场。一提到教育市场，这个产品成功率就会低于万分之一。不是绝对说教育市场没有成功的先例，笔者见到教育成功的品牌大多属于大品牌采取大资金的投入而促成的。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>有痛点的强需求穿衣、吃饭、坐公交这些是强需求，但这是广义上的强需求。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>有痛点的需求才是强需求！打个比方，打车是强需求，痛点是车难打，不知道司机在哪里？滴滴打车软件就是解决难打车这个痛点而出现的强需求产品。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>菜刀是刚需，痛点是菜刀使用过程总需要磨刀，而一款微创新的陶瓷刀就解决了磨刀这个痛点，因为这是一款永远不需要磨刀的陶瓷刀。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>如何找到属于自己的“小众强需求”</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>80％的调查，调查不出用户的真实需求，这是因为——消费者自己都没有觉察到问题，更不用说提出需求了。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>我们团队提出一个比较好的方法——称之为“苛刻消费者”，也就是当我们找到小众市场后，把自己变成这个市场的一个苛刻消费者，亲自体验消费者的消费过程，从购物体验到使用（食用）体验，到效果追踪，亲历整个过程，以极度挑剔的眼光来批判产品，从而找出没有被满足的那些强需求。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>首先，找到你的菜——聚焦小众。然后，找到他们的痛——激发强需求！</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>案例1：摇摆床的弱需求市场</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>案例2：有机米的聚焦与强需求</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>企业决策者就纳闷了，这么好的有机米，为什么就是撬不动市场，找到老徐约茶详谈之后，老徐指出了本次营销上的三大雷区。雷区一：企业心太贪，目标人群范围太广，妄图打造人人都需要吃的产品，让“人人”都没有强烈需求性。雷区二：定价非常尴尬，即便是为了满足更多人贪便宜的价格心理，但是30元/斤的价格对很多家庭来说也是天价，要知道超市里每斤七八块钱买的可是五常大米。另一个关键是，这个价格出来后，总计不到100元的空间，让未来的代理商（合作伙伴）没有操作空间。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>雷区三：有机并非强需求，即便是精英阶层，对有机的理念也尚未达到十分认可的地步，况且“有机”二字的信赖度并不是那么高，目前的现状是——“有机”是一种生活方式，而绝非是一种强需求。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>经过老徐团队为期两周的缜密调查和思辨，老徐团队给出了两个直接的建议：“聚焦小众”，“挖掘小众强需求”。其一，聚焦小众。将这款有机米定位为“宝宝米”——食用人群锁定在0～5岁的宝宝们，这个年龄段的宝宝是整个家庭的“小皇帝”。中国的国情是：大人们吃糙一点没关系，大人们都想将最好的留给孩子们，而有机的“宝宝米”自然就有了丰沃的理论土壤。其二，激发强需。当这款“宝宝米”定位在这个年龄段的时候，就会面临一个巨大的竞争对手——奶粉，那么这款产品针对0～5岁的孩子的强需求在什么地方呢？我们发现两个现状。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>现状一，2岁以内喝奶粉的孩子，有15％会发生奶粉过敏、耐受情况，导致便秘、上火时有发生，而这个不能喝奶粉的“空档期”，这款“宝宝米”就能顺理成章介入进来，成为一个强需求的产品。现状二，2岁以上的孩子肠胃功能很弱，而这个阶段的家长大多是让孩子跟自己吃一样的饭菜，导致孩子胃口很差，吃不下饭。宝宝米此刻又成为大宝宝们的强需——常吃宝宝米能够促进孩子的食欲，间接避免孩子受到垃圾食品的诱惑。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>案例3：一款定位儿童的手动榨汁机强需是什么</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>2015年年初，一款儿童手动榨汁机厂家找到了老徐，这款产品外观可爱，体积小巧，是一款能够手动压榨水果的榨汁机。说到榨汁机，很多家庭都有，甚至原汁机、破壁机都进入厨房，而且都是电动的、智能的。但是这款产品不用插电，只需手摇即可压榨，全身都是可拆解的，使用的塑料件是安全、可降解材料的。企业最早的定位是：最安全的儿童榨汁机，结果投放市场很不理想，因为榨汁机市场竞争激烈，而且很多家庭已配备了榨汁机，很难引发家长单独为孩子购买一台榨汁机。老徐团队决定深度挖掘产品的强需求，最终我们发现了另一个强需求——亲子。试想一下，一个妈妈带着孩子亲自DIY一份果汁，是不是包含了浓浓的母爱和亲子时光。“亲子”这个强需求远远大于“榨汁机”这个强需求，所以最终我们在广告诉求中都是“亲子“的美好画面，强调的是培养孩子的动手能力。2015年度，企业凭借这款“强需求”产品，销售额突破了2亿元。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>创业者在产品创新的道路上有无数个方向，其实也是无数个陷阱，很多看起来挺美的创新实则完全不具备操作性。创业者切忌贪大贪多，要根据自身的实力——先选择强需求类型产品，再聚焦小众人群。</p>
</blockquote>
