<p>◆ 1 共享经济正在崛起</p>
<blockquote>
<blockquote>
<p>1　共享经济正在崛起</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在国外，共享经济两大代表企业Uber和Airbnb发展日渐成熟，而共享经济在中国的发展从租车和旅游领域发起，先后崛起的滴滴打车、木鸟短租、住百家等，后又进军餐饮、零售业、医疗教育，比如阿姨帮、闲鱼等后起之秀。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>其中旅游业发展最快，呈蓝海状态，其次是餐饮和社区，餐饮市场已饱和，发展较慢，重点是业务扩张，社区经济可开发性较强；而医疗教育等行业还存在很多问题，市场潜力较大，但目前开发的师资、医疗力量只是“冰山一角”，相对需求市场较小。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>从传统的创业经验来看，Airbnb的点子相当不切实际：谁会愿意出租自家的空闲房间给来自陌生城市的陌生人？再者，你扪心自问下：你会在租用别人家里的房间，而不是去住旅馆吗？</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在上海三口之家100个平方米两居室，基本没有闲置的卧室可以借出来，但是美国和中国不一样，他们的房子很大，所以卧室出租成了一个行业。最近，Airbnb进入中国的信息不胫而走。其实它就是把你家中多余的房间出租，用更低的价格租给那些找房间的人，很多人希望找到家的感觉，你可以在网上浏览到他家的样子，甚至是提供早餐的样子，最后你发现，搞不好还可以和这个人成为朋友。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>共享经济：要找到的方向必须是资源过剩的领域</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>无论是时间、物品、知识，只有他们出现盈余之后才会被共享出来。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>车是Uber的吗？当然不是，是上班族的或是专门做Uber的；房间是Airbnb的吗？医生是备孕帮的吗？这些都不是，但是它们可以为我所用，当它们为我所用的时候，闲置资源的价值就得到了最大化，这就是共享经济的核心模式。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>所以说资源过剩是一切共享经济的根基，如果没有这个先决条件，其他都是无效的。你要做的是搭建一个共享经济平台。当说到平台的时候，我们自然会想到网站、APP之类的，其实如果你想尝试共享经济，你甚至可以从大学开始，在学校操场上搭建一个闲置物品租赁平台，你找个桌子摆在人流量大的地方，请同学把自己闲置的物品都放在你这里来出租，有的是小说，有的是吉他，有的可能是iPad或者游戏手柄，而这个时候你就成了一个撮合供需两方的介质，这就是最原始的做法，而网站、APP只是将这种形式无限扩大并高效利用起来。</p>
</blockquote>
</blockquote>
<p>◆ 2 社群是下一个风口</p>
<blockquote>
<blockquote>
<p>社群是一群价值观相同的人愉快地玩耍，顺便做点生意</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>一个社群之所以能够成立，是因为大家认可共同的价值观而愉快地聚在一起，所以，社群可以通过不同的方式形成：信仰相同形成社群，兴趣相同形成社群，需求相同形成社群，利益相同也能够形成社群……但是，在同一个社群里的人们，价值观一定是互为认同的。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>如果你玩社群的第一目的是销售产品，不仅群友会抵触，群管理者也会对你有所排斥。 真正会玩社群的人会将商业存于无形之中，他给人一种可信赖的形象，并且顺便卖点自己的产品。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>如果你组建社群，就必须承担社群管理者的角色，你必须先搞清楚社群的三大要素。其一，你的社群价值是什么？</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>如果你的社群价值是为了推荐面膜，那么这个社群毫无价值，如果你的价值上宣扬“女性的独立自主”则有可能获得别人的认可。很多时候，请不要纠结在你的社群和你产品或者服务的直接关联点上，跳开产品看群员的共同需求才会真正创造价值。比如说，你是一个销售枕头的创业者，你的社群应该是“睡眠健康探讨群”，则有可能让人参与进来。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>其二，你的社群成员是谁？</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>物以类聚，人以群分，不要将不在一个频道的人放在同一个社群，比如说你做的是一个宝宝妈育儿交流群，就不该让男人在群内出现，更为重要的是你要清楚知道你的目标消费者是谁。如果你销售进口奶粉，你的社群成员就是0～2岁的宝宝妈；如果你销售儿童护眼台灯，你的社群成员就是5～10岁的宝宝妈；虽然都是宝宝妈，这两类人是对你产品的关注度完全不同。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>其三，怎么和群员达成链接？达成链接的方式一般由浅到深，由线上到线下，由浅信赖到深信赖。往往从一次群内或微信单独沟通开始，继而有机会、多频次的沟通，甚至线下见面，然后是一个体验装的体验，一次产品的交易，再是发觉他的生意天赋和他的创业价值，最后去考虑如何让他成为你的分销伙伴或者代理。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（4）线上社群的演变社群，尤其是非线性的线上社群，主要是微信群和QQ群，到底经历过哪些演变。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>社群1.0版本：广告群第一个版本，大家都经历过，即群主建群做广告的时代。这个时代很多人自己建过群，也被别人拉到过群里。我们见到最多的群就是，一个群主把大家拉到一个群，然后在群里喊，告诉大家一个好消息，我做微商了，我代理了某某产品，请大家多捧场，类似这样的群就是典型的广告群。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>这是1.0版本。1.0版本是中心化的，群主即中心，群主在群里说不要发广告了，发广告的都被踢掉，但是刚说完，自己的广告马上发了出来，因为群是他建的，他的地盘他做主，你不能发广告，他可以发。这是1.0版本，这实际上是线上社群的主流，占比非常高。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>社群2.0版本：兴趣群</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>社群1.0版本目前我们还在沿用，而2.0版本叫兴趣相同聊话题，它是去中心化的。2.0版本是什么模式，拿我们前一段时间开设的“爆品思维”探讨群来说，大家是因为“爆品思维”课走在了一起，通过线下认识了彼此，然后大家又在线上建了一个群，这个群就是去中心化的社群2.0版本，这个群有没有老徐在里面都不重要，大家因为共同的兴趣走到一起，可以相互碰撞和激发。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>社群3.0版本：重组社群</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>比如某个人建立了N个越野群，发现越野群的互动性和参与性在降低，这并不代表越野群的价值走低，而是需要进行“群重组”，每一次重组都是一次激活。常见的重组方式是按照区域或者越野车品牌进行重组，比如杭州越野群、南京越野群，这样重组的好处显而易见，更能让群友之间的价值最大化，一个能带给群友价值最大化的群，其群本身就有价值。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>建群或重组群，如果私心太重是做不了的，给群友之间充分的互动价值，不能说自己是主办方怎么怎么着，你可以收钱做活动编排，甚至不断插入广告，但是，一定要给群友有互动的时间和空间。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>案例51：爆品会如何玩社群</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>爆品会的社群是三步而成：其一，在线上社群里，招到对我们感兴趣的人；其二，将感兴趣的人约在一起通过线下社群进行学习和交流；其三，在线下产生强链接，并为强链接的群员提供服务和更高端的社群价值。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>社群是组织网络化，但如果只停留在网络上，社群是无法实现其价值的。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>如果是从来没有见过面的两个人，完全通过网络上的交流建立信任关系是有难度的。只有通过高频次的线下见面，进行深入的沟通和交流，挖掘出社群成员的核心需求，并提供愿意付费买单的产品或服务，社群才能变现。</p>
</blockquote>
