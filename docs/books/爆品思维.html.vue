<blockquote>
<blockquote>
<p>美女嫁得好的比例远超过高学历的女性，长得好看的产品如美女一样拥有先天的优势。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>长得美，我才会秀</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>许多年轻的消费者都有一个习惯，收到一件好看的产品后第一时间拿出手机拍照晒图，在这个过程中，使用者为产品进行了网络口碑式的宣传。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>一、产品颜值的自测法1.产品色彩回归纯色或较少色块；2.产品线条趋于柔和明快；3.产品外表趋于卡通或潮流文化。二、产品颜值的他测法1.听取陌生客户对产品的建议；2.放在互联网上听取大家对他的评价；3.买一堆竞争产品回来，比比看！</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>三、产品颜值的改进办法1.多看多学习国内外优秀设计师的作品，领悟设计的真谛；2.在借鉴中微创新；3.把专业的事情交给专业的人去做——让工业（外观）设计公司参与进来。</p>
</blockquote>
</blockquote>
<p>◆ 3 看体验——找到用户尖叫的理由</p>
<blockquote>
<blockquote>
<p>3　看体验——找到用户尖叫的理由</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>用户体验，即用户在使用一个产品或系统之前、使用期间和使用之后的全部感受，包括情感、信仰、喜好、认知印象、生理和心理反应、行为和成就等各个方面。通俗来讲， 用户体验就是“这个东西好不好用，用起来方不方便，有没有让用户觉得很爽”。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>案例7：三只松鼠的用户体验三只松鼠在电商当中，第一次把用户思维做到极致。他卖的不仅仅是坚果，还有用户体验，常规有十二件套：果壳袋、餐巾纸、分享袋、卡片等。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>他怎么把电商的体验做到极致呢？当别人都在拼价格的时候，他首次提到用户思维，他把所有员工招集到一起，把吃瓜子、吃坚果所有不爽的环节都列出来，有人说吃瓜子的瓜子皮没地方放，有人说吃核桃太硬没有工具……三只松鼠所有的创新都是基于微创新，所有的用户体验都不是高深莫测的。因为你要擦手，我给你备了湿纸巾；因为你要装果壳，我送一个果壳袋；因为你没有吃完，我送一个封口袋。当所有东西我都替你考虑到了，你会不会觉得很爽，会不会帮我传播？</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>案例8：海底捞的服务体验</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>先说排队这事，在海底捞排队就是用户体验最佳的体现之一。海底捞就餐区之外开辟了100多平方米让人坐着排队，饮料就有三种，瓜果也有三种；如果你想上网，旁边至少有8台电脑给你上网；如果你带小孩，有儿童专区可以供小孩玩耍，还可以把小孩托管给工作人员；等待的时候工作人员可以给你手机进行消毒；女性还可以做美甲。网上传得比较凶的一个故事是——海底捞附近的美甲店都关门了，因为都去海底捞免费美甲去了。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>再说就餐的过程，海底捞继续不断为你提供极致服务。坐下来的时候给你递上围裙；手机给你套上塑料薄膜；顺便递给你一块眼镜布；水果免费端到你面前；从来不需多说话，饮料喝到一半的时候肯定给你续上；菜可以点半份的，如果你点多了，服务员会小声提醒你不够吃再点；假如得知你今天生日，会送你果盘，并且好几个服务员围着你唱生日快乐歌，我见过一个50多岁的大姐被海底捞的服务感动得落泪。最后走的时候，海底捞工作人员有时候会送你一点爆米花之类的小礼物；工作人员会递给你免费停车的单据；电梯口工作人员按着电梯按钮礼貌地请你进去……</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>现在的创业者，总是比产品本身的价格，其实，如果你能给用户极致的消费体验，产品的价格就是第二层面的事情了。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>一、爆品为何要注重用户体验1.用户体验决定产品或服务的价值消费者在没有经过体验之前，所有对产品的了解是基于广告或别人推荐等抽象的介绍，在经历过用户体验后，消费者变成了使用者，对产品更有话语权，尤其是很多用户体验发生在购买之前，而这类用户体验就直接决定了产品的价值感。2.用户体验决定着是否值得传播社交化媒体的兴起助推许多企业主改进产品的用户体验，因为产品的体验好和不好完全由用户说了算，好的用户体验会迅速晒在朋友圈或微博，能够影响很多人，反之亦然。3.用户体验决定是否二次购买维护一个老顾客的成本是开发一个新顾客成本的1/8，改进用户体验是维护老客户的根本，而良好的用户体验助推老客户进行复购。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>二、爆品的用户体验怎么去做有人曾经说过：自己的文章总是最好的，对于企业主而言，自己发明创造的产品就是“自己的文章”，容不得别人指手画脚，越是这样的心态，就越难出爆品。老徐团队提出一个比较好的方法——邀请跟产品没有关联的人员（消费者），人数在20人左右，让大家来体验产品，以挑剔的眼光来批判产品，然后站在客观的角度来对产品提意见，企业然后汇总消费者意见从而找出没有被满足的那些用户体验。</p>
</blockquote>
</blockquote>
<p>◆ 4 看速度——快速抢占心智资源</p>
<blockquote>
<blockquote>
<p>4　看速度——快速抢占心智资源</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（1）何为心智资源心智资源也称为定位，也就是你大脑里想到某个概念、某个场景、某个品类的时候，第一个跳出来的品牌和产品，比如：送礼就送——脑白金；牙齿防蛀就用——高露洁；大自然的搬运工——农夫山泉。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>消费者讨厌复杂的信息，提倡简单；讨厌过多的选择，希望快速的决策，所以消费者不知不觉在大脑里储存了该品牌产品的一个位置，并对该位置进行归纳——这个位置的大小和其针对性的描述，即所谓的“心智资源”。比如：国酒＝茅台；最安全的汽车＝沃尔沃，烟台＝产苹果，天津狗不理＝最好吃的包子。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>商战的最高境界就是看谁能抢占消费者的心智资源， 所以爆品打造者要将竞争的重心，从市场转移到消费者的心智和头脑中去，捕捉到消费者的心智需求，牢牢地占有这个第一。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>抢占心智资源经常存在两个误区。1.抢占“心智资源”的片区越大越好其实在这个时代，心智资源的抢占已进入细分和差异化时代，你很难抢占“有机米”的心智资源，但是你可以抢占“有机宝宝米”“有机孕妇米”“有机老人米”等细分市场；同理，你也很难抢占到已被高露洁抢占的“防蛀牙膏”的心智资源，但是，你至少可以抢占“防牙结石牙膏”的细分领域，这一块“心智资源”尚未被人占领。2.抢占心智资源一定是要投入巨额广告抢占心智资源是一种观念，而抢占的方式是多样的。打广告只是其中一种，比如我们在终端门店的展示，销售人员的说辞，所有对外的微信微博等工具，无不是为了抢占目标消费者的心智资源。毋庸置疑，有针对性的、适当的广告投放，对快速抢占心智资源有着非常重要的作用。因为心智资源只有唯一和第一之说，你若是慢了速度，别人教育的声音够大就有可能压住了你的声音。</p>
</blockquote>
</blockquote>
<p>◆ 5 看价格——物超所值才畅销</p>
<blockquote>
<blockquote>
<p>5　看价格——物超所值才畅销</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>无论贫穷还是富有，人性永远贪图的是“物超所值”。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（1）何为物超所值</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>打个比方：你走进服装店，看到一款满意的服装，你摸上去感觉面料不错，依照以往的购物经验，你内心里给它的定价是200元左右，结果翻出价格牌，只要98元。这就叫“物超所值”，这样的价格会大大促进商品的购买。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（2）爆品的定价原则爆品要“爆”，所以绝对不能把价格抬得老高，我们建议 爆品的定价应该至少满足20％目标消费者，最好朝满足80％目标消费者考虑， 物超所值是爆品定价的基本原则，很多奢侈品无法成为爆品的一个原因是过高的定价。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>一、定价的几种方式1.成本加价法——这是个微利时代，毛利润＝产品零售价的30％以内；2.竞争参考法——根据竞争对手，以人群定位区分，以价格拉开差距；3.消费接受法——消费者最接受的价格是看到您的产品，并听到你的描述后一个比较接近的价格。4.爆品定价法——考虑未来的爆品战略，通过对以上三种方式对比，并结合电商平台现状的价格参考，最终给出具体的定价建议。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>二、微利时代的定价逻辑1.消费者不是傻子；2.活着最重要；3.赚该赚的钱。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>现在生产的产品成本，消费者的估算值已接近上下5％，所以在这种情况下，你怎么定价定得合适？干爆品绝对不是一味的低价，笔者也不是劝大家把价格调低而降低品质，我们需要了解消费者能接受的价格，从而打造物超所值的产品。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>三、越快到达消费者价格越合适原先的定价方式是这样的：1.传统型企业——省级代理——地市级代理——县区经销——零售终端——消费者2.简化型企业——地市级代理——零售终端——消费者3.过渡型企业——（电商/微商）——消费者4.互联网企业——消费者（粉丝）</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>四、定价的三个误区1.首批产品生产数量较少，所以定价略高</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>某个生产儿童智能玩具产品的企业找到我，聊天中得知他们首批生产1000套，成本是200元一个，零售价大约是成本乘以1.8，所以零售价卖360元左右，我问他如果生产1万套成本是多少钱一套，他说大约是150元/个，相对应的，就算也乘以1.8，零售价大约是270元。我直言不讳地说：是不是这么理解更合适，因为你首批生产量比较小，所以让消费者多花费近100元的冤枉钱。如果按照这种定价方式，爆品则永无出头之日，所以最好的办法就是按照批量化生产的成本来定价，不能按照小批量的试产来扰乱这款爆品的价格体系。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>2.这个定价一定要为了眼前能赚钱</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>干爆品，为了获益无可厚非，但是获益的方式有多种，有的人为了前端获益，有的人为了后端获益。小米公司在2015年下半年推出一款定价699元的空气净化器，行业里的人都知道这款产品小米是一分钱赚不到的，但是这款产品3个月换一次滤芯，滤芯作为高频次更换的耗材，小米有相当不错的利润空间。试问，当同等级的空气净化器都在1500元以上的时候，一款699元的空气净化器横空出世，它就注定了是一款爆品的定价方式。其实，想透了这个点，我们就知道定价是通往爆品的路径而已，现在的定价不赚钱或少赚钱，有的是为了积累客户，有的是为了推出迭代产品，有的是为了后端的耗材的消费。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>3.必须给中间商留下足够的空间</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>传统模式里，中间商利润越厚越好，只有不断用丰厚的利润刺激中间商才能刺激他们销售更多的产品。但随着消费者对价格敏感度的提升，消费者渴望适合的价格而爆品必须给予适合的价格。因此，无论是企业本身，还是中间商，都应该降低自己的利润要求，寻求合适的利润而不是一味寻求高利润。</p>
</blockquote>
</blockquote>
<p>◆ 第二章 打造爆品的六大要素</p>
<blockquote>
<blockquote>
<p>第二章　打造爆品的六大要素</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>06　爆品五大基因：匠心赋予、产品创新、价值外露、内容走心、渠道扁平。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>07　越早理解“用户思维”真谛，越早理解“暴利时代过去”，越早理解“匠人精神”本质，越早理解“基于强需的微创新”，就越有机会干出爆品……</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>08　一切不合理的定价，不人性的产品，缺乏设计和用户体验的产品类目里，皆有出现爆品的可能，诀窍无他——颠覆而已。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>09　传统电商是流量为王，打的是价格战；社群电商是内容为王，打的是逼格战。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>10　产品到达消费者手上的时间周期太长，以及中间环节太多导致价格不亲民，这两个核心要素导致传统渠道在移动互联时代很难出爆品。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>11　爆品的起始渠道越来越去传统化，传统电商和传统线下商超、连锁门店等红利期已过，无法成为爆品的起始渠道。唯有依托微商和社群电商渠道，爆品才能快速抵达消费者。</p>
</blockquote>
</blockquote>
<p>◆ 1 你是谁</p>
<blockquote>
<blockquote>
<p>1　你是谁</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>品牌和属性名就跟人的姓名一样，其中品牌是解决姓什么的问题，属性名是解决你叫什么名字的问题。打个比方：双汇是品牌，火腿肠是属性名。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>爆品不仅要品牌，还需要非常重视品牌。对很多新生品牌而言，品牌命名就是第一步。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>一些老牌企业的名字大都是朝着美和吉祥的方向而命名的，比如——老凤祥、红双喜、周大福、同仁堂。一听这名字，传统文化的感觉就出来了。20年前，中国企业刚刚导入品牌VIS系统，所以那时候很多人起名字往往是找一个好听的单词，然后翻译成中文。比如格力、海尔、贝斯特。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>而在移动互联网的今天，品牌命名也在发生着变化，现在很多品牌起名字不是图吉祥如意，也不是图洋里洋气，而是图简单直接。比如：“嘀嘀”“去哪儿”“饿了吗”“大姨吗”，这些都是一些早期看起来平常不入流的品牌名字，许多目前已经估值几十亿元甚至几百亿元人民币。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（2）产品属性命名：要让人知道产品到底是什么</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>对于现在到处是微创新的产品来说，产品叫什么确实是个问题。打个比方：将蓝牙音箱和台灯进行组合的创新产品，到底是叫台灯音箱，还是叫音箱台灯？还是叫智能台灯？</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>现在很多创新产品根本无法直接使用属性名来命名，因为属性命已经很难聚焦它的功能或定位，所以出现了细分属性名，甚至再度细分属性名。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>案例16：一款让人云里雾里的产品</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>产品名字叫“芬多精森林浴发生机”</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>这是个一眼就能看穿的时代，我哪有机会听你说三分钟芬多精是什么，森林浴发生机是什么。所以这个产品最大的问题是需要深沟通才能说清楚属性名，还没有讲需求、讲痛点、讲情怀，光属性名就讲三分钟，消费者是没有这个耐心的。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>新概念的产品需要一个教育的过程，虽然这类产品几乎进入了无竞争领域，但此类产品跟第一章所说的“强需求”格格不入，况且这样的属性命名，也决定了这款产品很难具备“爆品”的核心要素。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>所有的爆品，都是从挖掘品牌名和属性名开始的。品牌名要有时代感、有特点，属性名要通过细分或再度细分来确定，要易懂，要直接。如果你有一个不错的项目，请将品牌名和属性名大声讲给朋友们听，当你这样讲出来的时候，看看别人是不是会有一个鲜明的记忆，看看别人是不是已经清楚地知道这是一个什么产品。如果满足以上两个条件，这样的命名便是成功的。</p>
</blockquote>
</blockquote>
<p>◆ 2 你的消费者是谁</p>
<blockquote>
<blockquote>
<p>2　你的消费者是谁</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>所有成功的爆品，都有一个共性——决策者都能很清楚地跟你描述他的消费者，而且，至少15分钟。你要清楚知道你的消费者的生活形态和生活轨迹，害怕什么和喜欢什么，他什么时候使用你的产品，使用的目的，以及什么时候分享。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>史玉柱做脑白金、黄金搭档的时候，考核业务人员有一个判断标准，就是你能不能很生动很形象地描述你的客户15分钟，从他住多少平方米的房子，拿多少钱的月薪，几点钟起床，甚至用什么牌子的牙膏，蹲在马桶上的时间是多少，早餐吃什么，坐什么车上班，上班时间、路程大概是多少，喜欢看什么报纸、看什么新闻，午餐吃什么，晚餐吃什么，几点睡觉，等等。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（1）人群聚焦方法之一：5岁定律研究营销之前先研究人，因为营销即人性。时代怎么更迭，人性自古至今没有变化过，成功的爆品操盘者都是把人性研究到了极致。而研究人性最怕的不知道谁才是我们研究的对象，我们很多人开始干爆品的时候容易犯一个毛病，就是认为谁都是“我的菜”，小到8个月，大到80岁，谁都想要，但往往是谁都不是“你的菜”。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>这个时代你需要为特定的人群提供服务或产品。打个比方，如果您销售一款沙发，如果跟60后沟通，用的销售术语一定跟实用性、身份感、性价比关联在一起；但如果这款沙发卖给90后，销售术语会从功能性、个性化、色彩感联系在一起。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>不要试图将同一个产品卖给两个年代的人。60后和70后相差一个代沟；80后就要分85前和85后，同样是80后，消费意识和消费行为差别很大；即将成为社会主流的90后，有专家认为他们每3年就有代沟产生。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>如果你的爆品是按照年龄段划分的，最有效的办法是把你的人群年龄段控制在5岁，“5岁定律”是我第一次提倡的，这也符合“少即是多”的聚焦定律。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>当你的人群更精准的时候，你就会发现他们有共同的烙印。如果你要跟他们讲情怀，情怀应该那样讲；传输价值观，价值观应该符合这个年龄段的共性。人群聚焦第一点： 把他们划分在5岁年龄段内，试着去走进这些人，深入到他们的内心，然后，再用15分钟描述他们。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（2）人群聚焦方法之二：同类人定律</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>你一定见过某些品牌的产品，并不是按照严格的年龄来划分的，他们往往按照兴趣爱好或需求来划分，我们称之为同类人定律。比如Jeep大切诺基，60后、70后、80后我都见有人开，因为开这种车的人有共同的爱好——喜欢越野，而且他们有着西部情怀，多是向往成为自由向上的男人。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>宁波有一个玩串玩壶的朋友，他身边全是喜欢玩串玩壶的朋友，他就是负责去云南、宜兴等地淘产品，然后朋友圈里分享，一堆人找他买，有时候单子下慢了还买不到。我问他哪些人是他的顾客，他的回答是什么人都有，男女老少都有，但他们都有一个共性，都是喜欢串喜欢壶。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>任何一个产品，都不要企图将产品卖给所有人，我们应该聚焦一类人群，要么控制在5岁左右的年龄段，要么锁定某一类族群，唯有如此，我们才能找到这类人群的共性，并与之产生共鸣。</p>
</blockquote>
</blockquote>
<p>◆ 3 消费场景设计</p>
<blockquote>
<blockquote>
<p>3　消费场景设计</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>消费场景即“消费者、消费时间、消费空间、动态行为、心理描述”这五者的综合体验，现实生活中消费场景无处不在，很多消费场景本身就存在了？</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>为什么中国的蜂蜜销量一直很大，这和一种“食用场景”和“消费习惯”息息相关——早晨喝蜂蜜排肠道，晚上喝蜂蜜睡得香。所以好的产品一定要设置使用场景，没有场景的产品都不能成为爆品。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>消费场景，即：什么人，在什么时间、什么地点、如何使用（或食用）了某款产品，以及使用（或食用）这款产品的动机是什么？当创业者对这个描述得越具体、越生动，则消费场景越真实，最终展示的时候也越能打动人。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在互联网时代，消费者在下单前，无法多感官（触觉、嗅觉、味觉）了解我们产品的优点，我们就需要通过图文影音构建一个虚拟的场景，让消费者在内心产生价值认同或满足感。同时，创业者千万不要试图去扭转消费者的消费场景，我们需要的是用微创新参与到消费者的消费场景里去。</p>
</blockquote>
</blockquote>
<p>◆ 4 找到消费者痛点</p>
<blockquote>
<blockquote>
<p>4　找到消费者痛点</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在电视广告为王的传播时代，那时候的传播是灌输式的，品牌专家们都强调卖点，企业卖什么产品就强调产品的独特销售主张，所以企业就在那个阶段找产品的卖点，比如“充电5分钟，通话2小时”是OPPO手机的卖点，“一片顶三片”是盖中盖的卖点。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>痛点 是移动互联网时代的专属名词， 不是基于产品角度阐述产品的优异或特点，而是基于消费者需求层面强调消费者的感受。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>智能手机太贵是多数工薪阶层的痛点——小米解决了这个痛点。衬衫洗一次熨一次又累又烦是家庭主妇的痛点——V&amp;O解决了这个痛点。宝宝吃奶粉容易上火便秘是宝宝妈的痛点——宝宝米解决了这个痛点。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>案例23：“爆品思维”找到企业家的痛点我们在推广“爆品思维”课程的时候，做了一个易企秀，详细地描述了现阶段创业者的痛点，报名效果很好，其核心文案如下：生产商不敢批量生产；代理商不敢随便囤货；经销商钱包捂得很紧；渠道商等客等到花谢；电商行业过了红利期；微商行业可谓前途未卜……而移动互联网的兴起，让一切都变得不可捉摸——消费者消费方式发生转变，渠道结构也随之发生改变；移动互联网去中心化、去中介化愈演愈烈；众创众筹众包正在颠覆传统的生产和营销；共享经济将革命一切产能和服务过剩的公司与机构……</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>爆品思维代表着聚焦聚焦再聚焦；代表着以用户思维为导向的研发、生产与销售；代表着专注某一类用户、专注某个极致单品；代表着找到了用户的痛点、实现了用户的参与；代表着一款产品有可能干几个亿甚至几十个亿。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>痛点在哪里？痛点是机遇对人性的深度理解，对产品、竞争、市场的深度剖析；痛点存在消费者的意识里，需要你找到并且刺激这个痛点；痛点就是让消费者心一揪，然后觉得我很需要这款产品或服务。</p>
</blockquote>
</blockquote>
<p>◆ 5 创业者或团队的情怀故事</p>
<blockquote>
<blockquote>
<p>5　创业者或团队的情怀故事</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>产品比产品只能比价格，价格比到底就没有底线了，但是情怀不一样， 情怀是一个人用匠人精神生产产品的态度，是个人人格魅力的一种体现。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>案例24：戴赛鹰和他的三个爸爸空气净化器</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>案例25：保温童鞋的情怀故事</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>产品是可以仿制的，唯有情怀不可仿制。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>比产品更有价值的是亲情、友情、父爱、母爱等附加价值，而这些价值需要情怀来赋予，当创业者在创业初期什么都没有的时候，我们的故事就可以拿到钱，就可以获得第一批种子粉丝，关键是——真实的情怀要人感动，唯有如此才能唤起消费者的共鸣。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>案例26：奶茶招商也可以做情怀</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>从现在起，在销售产品之前，先销售自己的匠人匠心。</p>
</blockquote>
</blockquote>
<p>◆ 6 承接爆品的线上渠道</p>
<blockquote>
<blockquote>
<p>6　承接爆品的线上渠道</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>爆品怎么才能抵达消费者手上？</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在传统电商时代，很多店主受电商平台的游戏规则约束，所以推出了爆品，店主们期望通过一款爆品吸引巨大的流量并且获得电商平台的权重。因此，那个阶段的爆品，到达消费者手上主要是看价格，比价格时代比着比着就无节操、无底线了——九块九丝袜包邮、1折起童鞋包邮的时代。虽然引来了流量，却丢失了利润，企业主挖空心思地节约成本，粗制滥造、假冒伪劣就在所难免。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在传统实体渠道时代，很多大卖场、便利店、加盟连锁店等为了吸引人流量，也采取过爆品策略，采取的方式是诸如“鸡蛋限时特价每斤2元”“榉木实木地板厂价大促”“麦辣鸡翅买一送一”。这些在传统渠道主要也是通过“爆品”的低价策略吸引人进来，继而通过其他产品和方式获得利润。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>老徐在规划爆品路径的时候说得最多的五句话，老徐称之为 “爆品35字决”：匠人匠心做产品；爆品思维找方法；微博微信干内容；微商社群起势头；传统电商来承接。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（1）微商社群起势头微商社群起势头，这句话的含义是指微商和社群电商将承担爆品的先前渠道。“起势”这个词语源于太极拳，是太极拳由无极到太极的开始，起势中的要领将贯穿于整套太极拳之中。一个太极拳起势，可以说“已尽含太极精蕴”。由起势入手，通过渐修顿悟，掌握其窍要，再由此切入，即可进入太极拳殿堂。而在 当前阶段，微商和社群电商是爆品起势头的渠道通路。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>微商最大的好处在于沉淀用户，实现分散的线上线下流量完全聚合，比传统电商更能精准找到用户群和互联大数据，从而大幅提升企业服务和订单量。当然，微商从诞生之初到现在，争议从未停止过。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>什么样的产品适合在微商圈里引爆？微创新的强需求产品——比如无钢圈文胸、精油香皂；能够被分销者高频次使用——比如卫生巾、化妆品；满足多级分销的价格体系——比如膏药、面膜；很好的视频感和场景感产品——比如钢丝袜、智能充电钱包。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>引爆适合微商操作的爆品有哪些要素？第一要素：你得打造或借力一个微商的团队（人）；第二要素：有一个适合微商团队开干的爆品（物）；第三要素：确定好分销模式中的价格体系（利益）；第四要素：制造出团队所需要的枪支弹药（内容）。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>微商引爆后的爆品会发生什么样的局面？竞争对手蜂拥而至，产品被模仿被抄袭；市场价格被搅局者打乱，低价产品横行；激情团队失去信心，核心人员离走创业；爆品成为昙花一现，往日风采再难出现。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>如何让爆品可持续性地发展？不断进行产品迭代，把微创新进行到底；时刻注意品牌积累，让微品牌屹立不倒；紧密团结核心成员，给微团队方向方法；紧跟社群电商步伐，将微营销落到社群。社群电商如何帮爆品起势头传统电商时代，消费者购买商品是因为消费者需要这个东西；社群电商时代，消费者购买商品是因为消费者相信某个人和组织。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>社群电商怎么理解？社群的两大核心是价值和用户，价值越大越能聚人，用户越多越有价值。社群电商是指使用社群为工具进行一些产品的销售，社群电商不是传统电商或者移动电商的颠覆模式，而是两者的一个深化延伸，它是一种商业意识形态的觉醒，是社群经济线上的表现形式。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>从某种意义上来说，社群电商是一套客户管理体系，通过客户的社群化充分激活品牌、产品、人际关系的沉淀客户。它抛弃了传统的客户管理方式，将每一个单独的客户通过社交工具进行了社群化改造，利用社会化媒体工具充分调动社群成员的活跃度和传播力。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>社群电商模型不仅适用于传统电商，也适用于移动电商，甚至它也适用仅仅通过社交工具进行销售的微商。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>社群电商中容易成功的爆品有哪些？</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>其一，口碑性农产品，比如橙子、苹果、樱桃、杨梅、鸡蛋等。这些产品通过社群大V的推广，辅以内容方面的原生态场景展示，容易成为爆品。其二，微创新的刚需单品，比如车载空气净化器、防臭袜子、功能内裤、智能水杯、创新牙膏、创新枕头、健康棉被、无钢圈文胸等。其三，基于微定制的产品，比如定制类衬衫、个性化眼镜、定制类音箱、有签名的紫砂产品、专为创业者准备的礼物酒等。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>社群电商的变现方式有哪些？</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>方法1：通过意见领袖的影响变现</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
