<p>（1）好看的东西更好卖的本质是什么</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>生物对美的追求是由性选择造成的，也就是说只有有性繁殖的生物具有达尔文意义上“美”的概念，个体的“美”或“丑”都是在性选择过程中产生的概念。</p>
