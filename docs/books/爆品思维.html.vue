<p>◆ 4 内容为王是铁律</p>
<blockquote>
<blockquote>
<p>“内容为王”这四个字诞生于PC互联网时代，当时意思是指网站的生存之道在于网站的内容质量，提供优质的网络资源予用户浏览是一个网站的根基！“提高用户体验”已成为网站建设和生存的关键，让用户找到自己想找的东西，能从网站上获取有价值的资料是一个网站存在的基础！所以那个时代提倡内容为王。而在移动互联时代，“内容为王”被重新定义， 是指广告主或流量主为了获得更多的眼球关注，而采取的高品质、创意性的图文影音等内容来黏合用户 。一切以流量或推销为目的的网络内容皆可以定义为内容。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>2014年7月25号，我开创了史上最贵的茶局——5000元/小时。首先，我们在《上海铁道》上打了一次广告，这个广告是《品牌策划界的奇葩——老徐和他的方法论》。在这里我提出一个很犀利的观点——没有大师也没有大众，以及沉淀的知信力方法论。2014年7月份，当时我的微信好友是240多人。这个广告打完，一个月不到，马上冲破5000人。所以好的内容就能引爆。这篇文章谁关注的最多，就是传统企业转型、认可我们匠人精神的企业主，而有情怀内容的文章就是一个爆点。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>老徐下午茶开创了很多奇迹，以前约人经常约不来，自从收费以后，大家经常可以看到，这个老总来喝茶，那个老总也来喝茶，所以就都坐不住了，主动找了过来。这个就叫营销。主动打电话过去，那个是推销。</p>
</blockquote>
