<p>◆ 3 产品众筹将颠覆传统</p>
<blockquote>
<blockquote>
<p>中国最早、最成功的众筹是谁发起的？答案是——住持和尚发起的寺庙众筹。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>他输出的价值就是信仰。“我不出一分钱，但是我做出来的事是和大家分享的。”所以寺庙众筹给我们的启发是：能不能做出一个有信仰的东西？而对创业者而言，你可以通过众筹来筹钱、筹人，但是你所能提供的价值是什么？</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（2）产品众筹的特征低门槛 ：无论身份、地位、职业、年龄、性别，只要有想法、有创造能力都可以发起项目。多样性 ：众筹的方向具有多样性，在国内众筹网站上的项目类别包括设计、科技、音乐、影视、食品、漫画、出版、游戏、摄影等。依靠大众力量 ：支持者通常是草根民众，而非公司、企业或是风险投资人。注重创意：发起人必须先将自己的创意（设计图、成品、策划等）达到可展示的程度，才能通过平台的审核，而不单单是一个概念或者一个点子，要有可操作性。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>因为C2B就是直面消费者，省略了所有的中间环节，其所带来的成本削减与渠道精简几乎是所有企业梦寐以求的。目前正兴起的众筹，首先就切中了所有企业这一痛点。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>众筹还解决了这些企业最头疼的问题——库存。过大库存量压断现金流，从而导致企业消亡的案例举不胜举。这样惨痛的教训也让很多企业在创业初期无法确定应该备多少库存，往往大业未展就束手束脚，而众筹，正是利用精巧的模式设计，完美地避开了库存量的问题，也是企业迈向C2B的重要一步。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>众筹所带来的C2B模式，就是消费者先提供订单，再由企业来生产，尤其适合智能创新类产品、家居生活类、季节性食品和非刚需产品。为什么现在已经越来越少的创业者聚焦刚需产品？因为刚需产品已经轮不到你去做了。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>众筹成功有三大基础：①有一款真正的爆品；②让参与者有被打动的瞬间；③让参与者有特别愿意相信你的地方。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>网上产品众筹的价格一般不超过1000元</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>举例来说，奇葩团私享会经常在普锐斯红酒会所举办，这个会所的老板娘特别聪明，所有去会所的人，她都拿一点红酒给你尝一尝，她的会所装修得也挺好，所以她给你感觉有三个：第一，我是有“庙”的地方；第二，我红酒很好喝；第三，我这个人不错。见面一谈，这个人一眼看上去就是面善的人。在这个“半熟”的基础上，她推出了一种众筹方式，只要投资88888元，受益有三种：第一种受益——十年内，每年送100瓶法国酒庄的红酒给你；第二种受益——十年当中产生的收益，你可以占股分利；第三种受益——在法国那边有一块地，你去法国随吃随喝随住，一年最多7天。一年不到已经完成了近2000万元的众筹。</p>
</blockquote>
