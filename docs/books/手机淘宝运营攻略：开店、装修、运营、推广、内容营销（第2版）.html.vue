<p>淘宝的自有流量不够用之后，开始采购外部流量，以单击付费形式销售给卖家，这就是无线的站外流量。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>目前已经上线在用的媒体APP包括：暴风影音、墨迹天气、内涵段子，即将上线的还有今日头条</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>质量得分影响因素虽然直通车后台只看到一个质量得分，但真实的情况是，在关键词推广的情况下，无线直通车和PC直通车有两个不同的质量分计算方法，并且二者互相独立。这解答了很多朋友的疑惑：开通无线端直通车后，为什么单击率大幅增高，质量分却没有上涨？</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>和PC端一样，影响无线端质量得分的TOP3因素，分别如下。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>1.文本相关性</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>和PC端相比，无线端的文本相关性更为重要。这里的文本相关性，主要指的是宝贝的创意标题。因为在直通车位置上，显示的是最多20个字的直通车创意标题，而非原标题。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>文本相关性，不仅仅是关键词要尽量在标题中出现，更是要卖点词在标题中出现。卖点词的相关性高（例如条纹衫的卖点为“经典”，只要创意标题中有“经典”，那么所有含有“经典”的关键词相关性都会较高），会极大提升该关键词的基础质量得分。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>2.单击率</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>任何时候，单击率都是质量得分最重要的因素，毋庸置疑。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>第一，无线端的单击率，因为众所周知的屏幕小、宝贝少的原因，单击率普遍很高。所以，在判断你的单击率是否合格的时候，标准也应该相应有所提高。这里给出作者的经验值：2%属于合格，3%以上属于优秀，如果1%以下属于很差了（声明：这是普遍意义下，某具体类目可能有所偏差）。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>第二，无线端的单击率，同时也要区分开站内和站外，还要排除掉PC端单击数据的影响。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>3.转化率</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>创意标题更加重要专业车手，使用直通车的创意标题，一般是作为宝贝标题的有效补充，或者是当作纯促销用语，例如“月销万件，好评如潮，不能再降了”之类的短语。而直通车的创意标题，自从无线端直通车开通后，其重要性显着提升。原因有以下3点。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（1）PC端的创意标题，上限20个字，但是最多显示17个字，其他都被隐藏了，而无线端全部显示</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（2）PC端直通车位置和自然搜索结果位置区分开来，而无线端直通车位和搜索结果位是融合在一起的，但用户对此无意识。（3）无线直通车的展示规则，并不是单纯由“出价×质量分”来决定。还有一个基于创意标题相关性的过滤规则。也就是说，即使你对某关键词出高价，也可能因为你的标题相关度低而无法展现。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>一直以来，匹配方式分为“精确匹配、中心词匹配、广泛匹配”3种。在PC端，这三者的差别很明显。但对于无线端，其差别正在缩小甚至消失。因为，无线端的直通车更强调精准性。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>假如，卖家购买竞价词是“武大郎烧饼”，而买家搜索词为“武大郎”。如果你匹配方式设为精准，那么PC端和无线端都一样无展现；如果匹配方式设为中心，此时竞价词的中心是产品名称“烧饼”，无法和搜索词匹配上。因此，PC端和无线端一样，都无展现。而在广泛匹配情况下，根据匹配的原则，PC端将获得展现，去淘宝搜索一下“武大郎”就能看到直通车位置上许多卖“武大郎烧饼”的宝贝；但是无线端，即使出价很高，也可能不会展现。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>这是由于无线端不仅会考虑出价和质量分，它还进一步考虑了用户体验。当用户搜索“武大郎”时，一个非武大郎牌的烧饼，从搜索相关性角度来讲是很差的，因为从字面上毫无相关性。所以系统鉴定后，会放弃直通车位向该买家的展现机会，如果没有合适直通车广告，甚至会放弃此次直通车广告展现，只保留更相关的自然搜索结果，从而使得直通车宝贝无法从该关键词上获得展现。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>无线端的广泛匹配，可能往往会由于系统对搜索相关性提高了要求，从而退化为中心词匹配，甚至精确匹配。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>无线端的直通车，3种匹配方式获得展现的能力区别已经缩小，无须太在意。最简单有效的做法是全使用中心词匹配。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>无线直通车出价技巧</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>无线端单击单价较低，原因总结起来有3个。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（1）为了吸引更多卖家参与无线端直通车的投放。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（2）在整个竞争队列中，由于一部分选手的出局，使得留下来的选手，在扣费上普遍降低。同一个竞价词下，不是所有参与出价的宝贝都显示。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（3）竞争的分散和不充分。简而言之，目前参与无线直通车投放的卖家还不多，竞争不充分，价格就会便宜。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>无线直通车的特点是你出价高，但是扣费往往比出价低很多。所以，通用的出价建议如下。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（1）可以大胆一点出价。这里给一个经验值供参考：无线出价＝PC端出价的150%。例如某个词在PC上出价1元，无线里就可以出1.5元。（2）关键词刚刚投放时，基础出价以比市场均价的110%为基准。精准长尾词，可以在基础上再上浮到140%。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（3）给自己设定一个合理的心理出价上限，热词量力而行，不要轻易超过。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（4）后续优化时，根据质量分变化来调整，质量分上涨，则价格可下调，否则上调。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>无线直通车选词技巧选词，被大家熟知的常规注意事项除了有展现、单击、转化、有类目相关性、文本相关性之外，还有以下几点。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>1.用无线端飙升词来养词</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>飙升词，指的是展现量在很短时间内突然飙升的买家搜索词。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>这种词的来源往往是淘宝运营小二的人工干预，例如“626年中大促”，或者突发热点事件，例如热搜“汽车安全锤”。飙升词的优点是展现量大，竞争度小，价格便宜，非常有利于养词，能够提高质量得分。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>2.用标签引导词来获得展现</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在手机上打字通常很困难，所以手机淘宝用户更喜欢选择系统推荐的标签词来二次搜索。所以我们在直通车选词的时候，也要遵守这个原则来选。操作方法如下。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（1）把你的产品词或者“产品词＋修饰词”作为起点，去手机淘宝上搜索，把它下方的所有扩展标签词记录下来</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>并作为下一次的起点来再次搜索扩展</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（2）选择另外某种属性词组合，重复上一步操作，把结果词和之前结果合并再去掉重复的。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（3）重复第二步，直到词量达到200个，或者无新词可选。去掉一些属性不对的、相悖的、展现量太少的，剩下的就可以作为选词结果。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>3.用新版流量解析来过滤无效词</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>当确定候选词后，下一步工作是确定其有无足够的展现量，并且必须是无线端展现量。这依赖直通车后台的新版流量解析，它能告诉卖家任何竞价词的详细数据，是决策选词的必备工具。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>这里会告诉卖家，这个关键词，有多少无线端的展现量。如果太少就排除掉。给一个经验阈值，一天展现量要大于100，小于该阈值的，可以认为是无效的。对于较大类目的，阈值可以适当提高一些。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>无线直通车优化技巧直通车优化总结起来两件事，“换词”和“调价”。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>1.无线端调价思路无线端调价思路和PC端一致，总结起来有以下几点。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（1）当词不错（质量分高于7分）但是展现量不够时，不想删除的就加价。（2）当展现量够，但是单击率不够时，例如低于0.5%，降价。（3）当展现、单击都不错，但是转化能力差时，例如低于1%，降价。（4）当ROI效果好，加价，反之降价。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>2.无线端换词思路无线端换词思路和PC端有较大的不同，关键差别在于以下几点。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（1）一个词在流量解析有展现量，但不一定有无线端的展现量。所以不仅要看全网展现量，更要看无线端的流量占比。（2）一个质量分高且有全网展现量的词，因为无线端直通车文本相关性的过滤规则，虽然排名不错，也不一定在你的宝贝里能获得展现。（3）所以，无线端决定一个词是否保留，取决于以下几点。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>要看流量解析里词是否有无线端数据。有展现但是无线端0，果断删除。要看投放反馈数据，是否在7天内获得足够的展现量。如果一个词，价格和质量分都不错，但是一段时间后仍然无展现，就果断淘汰掉。实用规则：3天无展现的删除，7天无单击的删除，15天无成交的删除。</p>
</blockquote>
</blockquote>
