</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>网店市场定位的起点是网民的消费心理，只要把握了网民的消费心理，并借助恰当的手段把这一定位传递给目标网民即可。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>准确的市场定位是要掌握已存在于顾客心中的想法，打开顾客的联想之门，使自己提供的商品在顾客心目中占据有利的地位。</p>
