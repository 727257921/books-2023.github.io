<p>◆ 5.3 提升手机淘宝转化的方法</p>
<blockquote>
<blockquote>
<p>提升手机淘宝转化的方法</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>手机淘宝店铺的运营，简单来看，就是流量和转化这两件事。目前，很多店铺的流量都得到了提升，但手机淘宝的转化却成了大多卖家头疼的问题，下面介绍提升手机淘宝转化的方法。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>手机端顾客不像PC端那样容易看到很多产品的信息，所以一个能体现你产品特性和卖点的标题，就显得很重要。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>卖家在设置手机端的宝贝标题时，可以加入一些主要的词，也可以加入该产品的特点词。手机端并不需要像PC端那样为了搜索而抢更多的搜索热词，手机端主要是为了转化，所以加入产品的卖点元素是重点</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>手机搜索权重的宝贝描述强调的是加入手机详情页就可以增加权重，这里要强调的是如何优化好详情页，才能增加权重。很多店铺的卖家在做无线端的宝贝描述时，直接把PC端的手机描述导入到手机端的描述里面，直接导致了手机端宝贝描述整体过于冗长，其实手机端的宝贝描述一般做到6屏就可以了</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>很多手机淘宝的买家是不咨询客服，不长时间浏览的，他们的时间只有2～3分钟，所以卖家把握这段时间，把产品的一系列信息展示给客户，已经变得越来越重要了。目前在手机端出现了音频描述形式，显示为“听掌柜说”。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>手机淘宝在宝贝详情的第一页只是展示了一个评价，这个评价很关键。因为很多顾客为了节省流量，直接就看这么一个评价，所以在手机端，排名第一的评价一定要做好</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>排名第一的评价需要具备的要素有以下几点：买家的旺旺的级别；产品的相关性；物流的相关性；客服的服务态度；顾客的对产品的想法。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在手机端，你的客服反应时间越短，买家咨询和购买的可能性就越大。现在的千牛工作台有客服机器人，一定要应用起来。</p>
</blockquote>
</blockquote>
<p>◆ 5.4 淘宝网店的库存管理</p>
<blockquote>
<blockquote>
<p>一般来说，活动主打商品，在活动结束后，仍能保持很高的转化率，所以主打商品的数量准备多多益善。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>然后是其他热销商品，就是核心商品，应和活动主打商品尽可能关联，搭配套餐，内页推荐，手段很多种，这批核心商品，建议准备比平时多1～2个月销量的库存。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>考虑到大型活动的一些特性，建议活动当日有意识地控制在架商品数量。比如在双十二当日将在架商品数从500种降为300种，主要将那些销量不好，库存不多的商品下架。这样做的好处有如下几个。（1）降低仓库配货的难度：减少配货人员去翻冷门商品的可能性。（2）减少客户的选择面：选择不是越多越好，尤其是在很多人都有选择恐惧症的今天。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>英国科学家曾经做过一个实验，让一组人从3种巧克力里挑选一个，另一组则从17种巧克力里挑选。结果对自己选择的满意度，第一组远高于第二组。所以说，控制在架商品种类，缩小活动当日的选择面，加速客户下订单的决心，对于整个活动是很重要的一环，同时作为入口商品的关联销售是非常重要的。</p>
</blockquote>
</blockquote>
<p>◆ 5.5 维护老客户远胜开发新客户</p>
<blockquote>
<blockquote>
<p>淘宝店铺必须引入关系营销中的老客户维护策略，真正认识到老客户是店铺最重要的一部分财富，才是店铺长久发展的必由之路。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>这就是著名的“漏斗原理”。今天流失10个，同时又来10个，从表面上看销售业绩没有受到任何影响，而实际上为争取这些新客户所花费的宣传、促销的成本显然要比维护老客户昂贵得多，从投资回报程度的角度考虑是非常不经济的。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>留住老客户还会使宣传推广成本大幅度降低。发展一个新客户的投入是巩固一个老客户的5～9倍。在许多情况下，即使争取到一个新客户，也要经过一段时间才有可能发生消费行为。因此，确保老客户的再次消费是降低销售成本和节省时间的最好方法。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>老客户营销的方式有以下几种。1.老客户维护常用途径和办法维护老客户，要明确客户需求，积极满足客户需求。（1）发展会员法：普通会员和VIP会员，不同的会员享受不同的购物折扣，会员可分为付费会员和非付费会员。（2）购物积分法：这是维护老客户普遍应用的方法。根据每次所购商品的价值赠送不等的积分，积分累加可以享受不等的购物优惠或者是积分兑换等。（3）返送购物券法：购物时据购物商品的情况赠送不同价值的购物券，供客户下次购物使用。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>2.特殊买家特殊对待根据“二八原则”，公司利润的80%是由20%的客户创造的，哈佛商业杂志的一篇研究报告指出：多次光顾的买家比初次登门的客户可多为企业带来20%～80%的利润。所以善于经营的卖家要根据买家本身的价值和利润来对待买家，并密切关注高价值的买家，保证他们可以获得应得的优质服务和待遇，使他们成为店铺的忠诚客户。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
