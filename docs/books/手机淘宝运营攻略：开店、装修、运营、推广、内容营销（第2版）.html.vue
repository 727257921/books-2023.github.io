</blockquote>
<blockquote>
<blockquote>
<p>1.类目选择</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>一般选择类目的原则有以下几个。第一，有资源优势。比如，开服装店，自己家附近有大型衣帽批发市场；卖茶叶，家人、朋友有茶叶货源；卖玩具，自己家族里有玩具工厂等。第二，看好行业前景。比如几年前开始普及的智能手机、智能设备等。第三，兴趣爱好。比如因为喜欢陶瓷器皿，有这个兴趣，于是开始经营陶瓷器皿店铺。这种情况下如果一开始生意很艰难，但因为有兴趣而不容易放弃，以后就会慢慢好起来。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>2.从消费者的角度来思考</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>几乎没有人可以一次就把定位定得很准确，所以需要创业者在经营过程中不断思考，及时调整和优化自己店铺的定位。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
