<p>◆ 5.1 数据的采集和分析</p>
<blockquote>
<blockquote>
<p>数据的采集和分析</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>要进行数据的采集与分析，就需要明确数据采集与分析的流程，要了解数据对于卖家的用途是什么，是为了提高营业额还是为了了解店铺的被关注度，进而把这些问题的解答转化为一个数据采集和分析的初步方案</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>数据采集可以分成两部分来做，一部分是数据提取，另一部分是数据整理。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>数据分析同样可以分成两部分来做，一部分是数据挖掘，另一部分是数据分析。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（1）数据挖掘：是透过企业内储存的大量资料，以找出不同的客户，分析客户的喜好。通过分析企业原有数据，做出归纳性的推理，从中挖掘出潜在的需求模式。（2）数据分析：数据分析可帮助人们做出判断，以便采取适当行动。数据分析是组织有目的地收集数据、分析数据，使之成为信息的过程。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>对于多数淘宝卖家而言，在进行数据分析时都会先采集几个重要的数据：访客数、浏览量、跳失率、人均浏览量、平均停留时长（秒）、收藏量、浏览回头客、浏览回头率、平均访问深度等。这些数据通过数据分析软件“生意参谋”，可以非常直观地展现给卖家</p>
</blockquote>
</blockquote>
