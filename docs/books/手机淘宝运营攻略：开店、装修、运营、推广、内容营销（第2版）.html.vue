</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（2）专业牌。利用自己的专业知识博取顾客的信任，比如一些新技术产品或者操作复杂的产品。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>（3）特色牌。店里的产品可以做到“人无我有，人有我优”。</p>
