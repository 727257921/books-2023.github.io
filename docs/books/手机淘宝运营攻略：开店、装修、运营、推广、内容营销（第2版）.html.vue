<blockquote>
<p>淘宝达人有这么多好处，那么商家应该如何与他们合作来推广自己的商品或品牌呢？（1）改变自身思维。对于传统电商来说，一定要学会用互联网思维来卖货，抛弃以往的单一导购渠道推广，而将精力放在社交导购的场景化渠道上。（2）加强员工培养。对于淘宝店铺的运营人员来说，以往大家用得最多的就是搜索引擎推广和直通车了，如今，内容运营平台和数据分析工具成为了商家推广必不可少的新渠道，这些都是员工需要学习和加强的方面。</p>
