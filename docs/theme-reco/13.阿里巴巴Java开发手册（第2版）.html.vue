<h3 id="◆-1-4-oop规约" tabindex="-1"><a class="header-anchor" href="#◆-1-4-oop规约" aria-hidden="true">#</a> ◆ 1.4 OOP规约</h3>
<blockquote>
<blockquote>
<p>避免通过一个类的对象引用访问此类的静态变量或静态方法，造成编译器解析成本无谓增加，直接用类名访问即可。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>所有的覆写方法都必须加@Override注解。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>说明：getObject()与get0bject()的问题。一个是字母的O，一个是数字的0，加@Override注解可以准确判断是否覆盖成功。另外，如果在抽象类中对方法签名进行修改，其实现类会马上编译报错。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>对外部正在调用或者二方库依赖的接口，不允许修改方法签名，避免对接口调用方产生影响。若接口过时，则必须加@Deprecated注解，并清晰地说明采用的新接口或者新服务是什么</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>Object的equals方法容易抛空指针异常，应使用常量或确定有值的对象调用equals。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>所有整型包装类对象之间值的比较，全部使用equals方法。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>浮点数之间的等值判断，基本数据类型不能用==进行比较，包装数据类型不能用equals方法判断。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>浮点数采用“尾数+阶码”的编码方式，类似于科学计数法的“有效数字+指数”的表示方式。二进制无法精确表示大部分十进制小数</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在循环体内，字符串的连接方式使用StringBuilder的append方法扩展。说明：下例中，反编译出的字节码文件显示每次循环都会new出一个StringBuilder对象，然后进行append操作，最后通过toString方法返回String对象，造成内存资源浪费。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>final可以声明类、成员变量、方法及本地变量，下列情况使用final关键字。1）不允许被继承的类，如：String类。2）不允许修改引用的域对象，如：POJO类的域变量。3）不允许被覆写的方法，如：POJO类的setter方法。4）不允许在运行过程中给局部变量重新赋值。5）避免上下文重复使用一个变量，使用final描述可以强制重新定义一个变量，方便更好地重构。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>慎用Object的clone方法拷贝对象。说明：对象的clone方法默认是浅拷贝，若想实现深拷贝，需要覆写clone方法来实现域对象的深度遍历式拷贝。</p>
</blockquote>
</blockquote>
<h3 id="◆-1-5-日期时间" tabindex="-1"><a class="header-anchor" href="#◆-1-5-日期时间" aria-hidden="true">#</a> ◆ 1.5 日期时间</h3>
<blockquote>
<blockquote>
<p>说明：在日期格式化时，yyyy表示当天所在的年，大写的YYYY表示week in which year（JDK 7之后引入的概念），意思是当天所在的周属于的年份。一周从周日开始，周六结束，只要本周跨年，返回的YYYY就是下一年。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在日期格式中，分清楚大写的M和小写的m、大写的H和小写的h分别代表的意义。说明：日期格式中的这两对字母表意如下：1）表示月份，用大写的M；2）表示分钟，用小写的m；3）表示24小时制，用大写的H；4）表示12小时制，用小写的h。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>获取当前毫秒数：是System.currentTimeMillis(); 而不是new Date().getTime()。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>如果想获取更加精确的纳秒级时间值，则使用System.nanoTime的方式。在JDK 8中，针对统计时间等场景，推荐使用Instant类。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>不允许在程序的任何地方使用：1）java.sql.Date；2）java.sql.Time；3）java.sql.Timestamp。说明：第1个不记录时间，getHours()抛出异常；第2个不记录日期，getYear()抛出异常；第3个在构造方法super((time/1000 1000)时，在Timestamp属性fastTime和nanos中分别存储秒和纳秒信息。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>反例：在使用java.util.Date.after(Date)进行时间比较时，当入参是java.sql.Timestamp时，会触发JDK BUG(JDK 9已修复)，在比较时可能导致意外结果。</p>
</blockquote>
</blockquote>
<h3 id="◆-1-6-集合处理" tabindex="-1"><a class="header-anchor" href="#◆-1-6-集合处理" aria-hidden="true">#</a> ◆ 1.6 集合处理</h3>
<blockquote>
<blockquote>
<p>判断所有集合内部的元素是否为空，应使用isEmpty()方法，而不是使用size()==0的方式。说明：在某些集合中，前者的时间复杂度为O(1)，而且可读性更好。</p>
</blockquote>
</blockquote>
<h3 id="◆-1-7-并发处理" tabindex="-1"><a class="header-anchor" href="#◆-1-7-并发处理" aria-hidden="true">#</a> ◆ 1.7 并发处理</h3>
<blockquote>
<blockquote>
<p>获取单例对象需要保证线程安全，其中的方法也要保证线程安全。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>资源驱动类、工具类、单例工厂类都需要注意。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>线程资源必须通过线程池提供，不允许在应用中自行显式创建线程。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>使用线程池的好处是减少在创建和销毁线程上消耗的时间及系统资源，解决资源不足的问题。如果不使用线程池，则有可能造成系统创建大量同类线程而导致消耗完内存或者“过度切换”的问题。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>SimpleDateFormat是线程不安全的类，一般不要定义为static变量，如果定义为static，则必须加锁，或者使用DateUtils工具类。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>必须回收自定义的ThreadLocal变量，尤其在线程池场景下，线程经常会被复用，如果不清理自定义的ThreadLocal变量，则可能会影响后续业务逻辑和造成内存泄露等问题。尽量在代理中使用try-finally块回收。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在高并发场景中，同步调用应该考量锁的性能损耗。能用无锁数据结构，就不要用锁；能锁区块，就不要锁整个方法体；能用对象锁，就不要用类锁。说明：使加锁的代码块工作量尽可能的小，避免在锁代码块中调用RPC方法。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在对多个资源、数据库表、对象同时加锁时，需要保持一致的加锁顺序，否则可能会造成死锁。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>如果线程一需要对表A、B、C依次全部加锁后才可以进行更新操作，那么线程二的加锁顺序也必须是A、B、C，否则可能出现死锁。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在使用阻塞等待获取锁的方式中，必须在try代码块之外，并且在加锁方法与try代码块之间没有任何可能抛出异常的方法调用，避免加锁成功后，在finally中无法解锁。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>说明二：如果lock方法在try代码块之内，可能由于其他方法抛出异常，导致在finally代码块中，unlock对未加锁的对象尝试解锁，它会调用AQS的try Release方法（取决于具体实现类），抛出Illegal MonitorStateException异常。说明三：在Lock对象的lock方法实现中，可能抛出unchecked异常，产生的后果与说明二相同。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在使用尝试机制获取锁的方式中，进入业务代码块之前，必须先判断当前线程是否持有锁。锁的释放规则与锁的阻塞等待方式相同。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在并发修改同一记录时，为避免更新丢失，需要加锁。要么在应用层加锁，要么在缓存层加锁，要么在数据库层使用乐观锁，使用version作为更新依据。说明：如果每次访问冲突概率小于20%，则推荐使用乐观锁，否则使用悲观锁。乐观锁的重试次数不得小于3次。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>对于多线程并行处理定时任务的情况，当Timer运行多个TimeTask时，只要其中之一没有捕获抛出的异常，其他任务便会自动终止。如果使用ScheduledExecutorService，则没有这个问题。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>与资金相关的金融敏感信息，使用悲观锁策略。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>乐观锁在获得锁的同时已经完成了更新操作，校验逻辑容易出现漏洞。另外，乐观锁对冲突的解决策略有较复杂的要求，处理不当容易造成系统压力或数据异常，所以与资金相关的金融敏感信息不建议使用乐观锁更新。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>正例：悲观锁遵循“一锁二判三更新四释放”的原则</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>使用CountDownLatch进行异步转同步操作，每个线程在退出前必须调用countDown方法，线程执行代码注意catch异常，确保countDown方法被执行到，避免主线程无法执行至await方法，直到超时才返回结果。说明：子线程抛出异常堆栈，不能在主线程try-catch到。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>避免Random实例被多线程使用，虽然共享该实例是线程安全的，但会因竞争同一seed导致性能下降。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>Random实例包括java.util.Random的实例或者Math. random()的方式。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>volatile解决多线程内存不可见问题。对于一写多读，可以解决变量同步问题；但是对于多写，同样无法解决线程安全问题。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>当容量不够resize时，由于高并发，HashMap可能出现死链，导致CPU飙升，在开发过程中注意规避此风险。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>ThreadLocal对象使用static修饰，Thread Local无法解决共享对象的更新问题。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>这个变量是针对一个线程内所有操作共享的，所以设置为静态变量，所有此类实例共享此静态变量，也就是说，在类第一次被使用时装载，只分配一块存储空间，所有此类的对象（只要是这个线程内定义的）都可以操控这个变量。</p>
</blockquote>
</blockquote>
