<h3 id="◆-5-3-sql语句" tabindex="-1"><a class="header-anchor" href="#◆-5-3-sql语句" aria-hidden="true">#</a> ◆ 5.3 SQL语句</h3>
<blockquote>
<blockquote>
<p>1【强制】不要使用count(列名)或count(常量)来替代count(<em>)，count(</em>)是SQL92定义的标准统计行数的语法，与数据库无关，与NULL和非NULL无关。说明：count(*)会统计值为NULL的行，而count(列名)不会统计此列值为NULL的行。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>不要使用count(列名)或count(常量)来替代count(<em>)，count(</em>)是SQL92定义的标准统计行数的语法，与数据库无关，与NULL和非NULL无关。说明：count(*)会统计值为NULL的行，而count(列名)不会统计此列值为NULL的行。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>2【强制】count(distinct column) 计算该列除NULL外的不重复行数。注意，count(distinct column1, column2)，如果其中一列全为NULL，那么即使另一列有不同的值，也返回为0。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>3【强制】当某一列的值全为NULL时，count(column)的返回结果为0，但sum(column)的返回结果为NULL，因此使用sum()时需注意避免NPE问题。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>正例：可以使用如下方式避免sum的NPE问题：SELECT IFNULL(SUM (column), 0) FROM table;</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>4【强制】使用ISNULL()判断是否为NULL值。说明：NULL与任何值的直接比较都为NULL。1）NULL&lt;&gt;NULL的返回结果是NULL，而不是false。2）NULL=NULL的返回结果是NULL，而不是true。3）NULL&lt;&gt;1的返回结果是NULL，而不是true。反例：在SQL语句中，如果在null前换行，则会降低可读性。select * from table where column1 is null and column3 is not null; 而ISNULL(column)是一个整体，简捷易懂。从性能数据上分析，ISNULL(column)的执行效率更高一些。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>6【强制】不得使用外键与级联，一切外键概念必须在应用层解决。说明：以学生和成绩的关系为例，学生表中的student_id是主键，成绩表中的student_id为外键。如果更新学生表中的student_id，则同时触发成绩表中的student_id更新，即为级联更新。外键与级联更新适用于单机低并发，不适合分布式、高并发集群；级联更新是强阻塞，存在数据库更新风暴的风险；外键影响数据库的插入速度。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>7【强制】禁止使用存储过程，存储过程难以调试和扩展，更没有移植性。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>8【强制】当订正数据（特别是删除或修改记录操作）时，要先select，避免出现误删除，确认无误才能执行更新语句。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>11【推荐】in操作能避免则避免，若实在避免不了，则需要仔细评估in后面的集合元素数量，控制在1000之内。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>12【参考】如果有国际化需要，那么所有的字符存储与表示，均以UTF-8编码，注意字符统计函数的区别。说明：SELECT LENGTH(&quot;轻松工作&quot;)；返回为12。SELECT CHARACTER_LENGTH(&quot;轻松工作&quot;)；返回为4。如果需要存储表情，那么选择utf8mb4进行存储，注意它与UTF-8编码的区别。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>13【参考】TRUNCATE TABLE比DELETE速度快，且使用的系统和事务日志资源少，但TRUNCATE无事务且不触发trigger，有可能造成事故，故不建议在开发代码中使用此语句。说明：TRUNCATE TABLE在功能上与不带WHERE子句的DELETE语句相同。</p>
</blockquote>
</blockquote>
