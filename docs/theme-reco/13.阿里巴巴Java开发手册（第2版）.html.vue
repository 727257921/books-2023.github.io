<h3 id="◆-1-10-前后端规约" tabindex="-1"><a class="header-anchor" href="#◆-1-10-前后端规约" aria-hidden="true">#</a> ◆ 1.10 前后端规约</h3>
<blockquote>
<blockquote>
<p>前后端交互的API，需要明确协议、域名、路径、请求方法、请求内容、状态码和响应体。说明：1）协议：生产环境必须使用HTTPS。2）路径：每一个API都需对应一个路径，表示API具体的请求地址。 a）代表一种资源，只能为名词，推荐使用复数，不能为动词，请求方法已经表达动作意义。 b）URL路径不能使用大写，单词如果需要分隔，统一使用下画线。 c）路径禁止携带表示请求内容类型的后缀，比如&quot;.json&quot;,&quot;.xml&quot;，通过accept头表达即可。3）请求方法：对具体操作的定义，常见的请求方法如下。 a）GET：从服务器取出资源。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>b）POST：在服务器新建一个资源。 c）PUT：在服务器更新资源。 d）DELETE：从服务器删除资源。4）请求内容：URL带的参数必须无敏感信息或符合安全要求；body里带参数时必须设置Content-Type。5）响应体：响应体body可放置多种数据类型，由Content-Type头来确定。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>当服务端发生错误时，返回给前端的响应信息必须包含HTTP状态码、errorCode、errorMessage和用户提示信息四部分。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>正例：常见的HTTP状态码如下。1）200 OK: 表明该请求被成功地完成，所请求的资源发送到客户端。2）401 Unauthorized: 请求要求身份验证，常见于需要登录而用户未登录的情况。3）403 Forbidden：服务器拒绝请求，常见于机密信息或通过复制其他登录用户链接访问服务器的情况。4）404 Not Found: 服务器无法获得所请求的网页，请求资源不存在。5）500 Internal Server Error: 服务器内部错误。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在前后端交互的JSON格式数据中，所有的key必须都为小写字母开始的lowerCamelCase风格，符合英文表达习惯，且表意完整。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>[插图]</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>errorMessage是前后端错误追踪机制的体现，可以在前端输出在type=&quot;hidden&quot;文字类控件中，或者用户端的日志中，帮助我们快速定位问题。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>对于需要使用超大整数的场景，服务端一律使用String字符串类型返回，禁止使用Long类型。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>说明：Java服务端如果直接给前端返回Long整型数据，JS会自动转换为Number类型（注：此类型为双精度浮点数，表示原理与取值范围等同于Java中的Double）。Long类型能表示的最大值是263−1，在取值范围之内，当超过253(9007199254740992)的数值转化为JS的Number时，有些数值会有精度损失。扩展说明，在Long取值范围内，任何2的指数次整数都是绝对不会存在精度损失的，所以说精度损失是一个概率问题。若浮点数尾数位与指数位空间不限，则可以精确表示任何整数，但很不幸，双精度浮点数的尾数位只有52位。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>反例：通常在订单号或交易号大于或等于16位时，大概率会出现前后端单据不一致的情况，比如，&quot;orderId&quot;: 362909601374617692，前端拿到的值却是362909601374617660。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>当HTTP请求通过URL传递参数时，不能超过2048字节。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>说明：不同浏览器对于URL的最大长度限制略有不同，并且对超出最大长度的处理逻辑也有差异，2048字节是取所有浏览器的最小值。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>反例：某业务将退货的商品id列表放在URL中作为参数传递，当一次退货商品数量过多时，URL参数超长，传递到后端的参数被截断，导致部分商品未能正确退货。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>【强制】HTTP请求通过body传递内容时，必须控制长度，超出最大长度后，后端解析会出错。说明：Nginx默认限制为1MB，Tomcat默认限制为2MB，当确实有业务需要传递较多内容时，可以调大服务器端的限制。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在翻页场景中，如果用户输入的参数少于1，则前端返回第一页参数给后端；如果后端发现用户输入的参数大于最后一页，则直接返回最后一页。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>服务器内部重定向必须使用forward；外部重定向地址必须使用URL统一代理模块，否则会因线上采用HTTPS而导致浏览器提示“不安全”，并且还会带来URL维护不一致的问题。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>服务器返回信息必须标记是否可以缓存，如果可以缓存，那么客户端可能会重用之前的请求结果。说明：缓存有利于减少交互次数，减少交互的平均延迟。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>[插图]</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在接口路径中不要加入版本号，版本控制在HTTP头部信息中体现，有利于向前兼容。说明：当用户在低版本与高版本之间反复切换时，会导致迁移复杂度升高，存在数据错乱的风险。</p>
</blockquote>
</blockquote>
<h3 id="◆-1-11-其他" tabindex="-1"><a class="header-anchor" href="#◆-1-11-其他" aria-hidden="true">#</a> ◆ 1.11 其他</h3>
<blockquote>
<blockquote>
<p>任何数据结构的构造或初始化，都应指定大小，避免数据结构无限增长而耗尽内存。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>8【推荐】及时清理不再使用的代码段或配置信息。说明：对于垃圾代码或过时配置，坚决清理干净，避免程序过度臃肿，代码冗余。</p>
</blockquote>
</blockquote>
<h3 id="◆-2-1-错误码" tabindex="-1"><a class="header-anchor" href="#◆-2-1-错误码" aria-hidden="true">#</a> ◆ 2.1 错误码</h3>
<blockquote>
<blockquote>
<p>当全部正常，但不得不填充错误码时，返回五个零（00000）。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>错误码为字符串类型，共5位，分为错误产生来源、四位数字编号两部分。说明：错误产生来源分为A、B、C三种，A表示错误来源于用户，例如参数错误、用户安装版本过低、用户支付超时等；B表示错误来源于当前系统，例如业务逻辑出错、程序健壮性差等；C表示错误来源于第三方服务，例如CDN服务出错、消息投递超时等；四位数字编号从0001到9999，大类之间的步长间距预留100。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>错误码不能直接输出给用户作为提示信息使用。说明：堆栈、错误码（errorCode）、错误信息(errorMessage)、提示信息（userTip）是一个有效关联并互相转义的和谐整体，但请勿越俎代庖。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>错误码分为一级宏观错误码、二级宏观错误码、三级宏观错误码。说明：在无法确定的错误场景中，可以直接使用一级宏观错误码，分别是：A0001（用户端错误）、B0001（系统执行出错）、C0001（调用第三方服务出错）。</p>
</blockquote>
</blockquote>
<h3 id="◆-2-2-异常处理" tabindex="-1"><a class="header-anchor" href="#◆-2-2-异常处理" aria-hidden="true">#</a> ◆ 2.2 异常处理</h3>
<blockquote>
<blockquote>
<p>2【强制】异常被捕获后不要用来做流程控制和条件控制。说明：异常设计的初衷是解决程序运行中的各种意外，且异常的处理效率比条件判断方式要低很多。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>捕获异常是为了处理异常，不要捕获了却什么都不处理而抛弃之，如果不想处理它，请将该异常抛给它的调用者。最外层的业务使用者必须处理异常，将其转化为用户可以理解的内容。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在事务场景中，抛出异常被catch后，如果需要回滚，那么一定要注意手动回滚事务。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>finally块必须对资源对象、流对象进行关闭操作，如果有异常就要做try-catch操作。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>不要在finally块中使用return。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>说明：try块中的return语句执行成功后，并不马上返回，而是继续执行finally块中的语句，如果此处存在return语句，则在此直接返回，无情地丢弃try块中的返回点。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>在调用RPC、二方包或动态生成类的相关方法时，捕获异常必须使用Throwable类拦截。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>正例：使用JDK 8的Optional类防止产生NPE。</p>
</blockquote>
</blockquote>
<h3 id="◆-2-3-日志规约" tabindex="-1"><a class="header-anchor" href="#◆-2-3-日志规约" aria-hidden="true">#</a> ◆ 2.3 日志规约</h3>
<blockquote>
<blockquote>
<p>所有日志文件至少保存15天，因为有些异常具备以“周”为频次发生的特点。对于当天日志，以“应用名.log”保存在“/home/admin/应用名/logs/”目录下，过往日志格式： {logname}.log.{保存日期}，日期格式：yyyy-MM-dd。[插图]</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>根据国家法律，网络运行状态、网络安全事件、个人敏感信息操作等相关记录，留存日志的时间不少于六个月，并且进行网络多机备份。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>应用中的扩展日志（如打点、临时监控、访问日志等）命名方式：appName_logType_logName.log。logType为日志类型，如stats/monitor/access等；logName为日志描述。这种命名的好处是通过文件名就可以知道日志文件属于哪个应用，哪种类型，有什么目的，这也有利于归类查找。说明：推荐对日志进行分类，如将错误日志和业务日志分开存放，既便于开发人员查看，也便于通过日志及时监控系统。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>当输出日志时，字符串变量之间的拼接使用占位符的方式。说明：因为String字符串的拼接会使用StringBuilder的append()方式，所以有一定的性能损耗。使用占位符仅是替换动作，可以有效提升性能。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>避免重复打印日志，否则会浪费磁盘空间，务必在日志配置文件中设置additivity=false。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>可以使用warn日志级别记录用户输入参数错误的情况，避免当用户投诉时无所适从。</p>
</blockquote>
</blockquote>
<h2 id="◆-第3章-单元测试" tabindex="-1"><a class="header-anchor" href="#◆-第3章-单元测试" aria-hidden="true">#</a> ◆ 第3章 单元测试</h2>
<blockquote>
<blockquote>
<p>好的单元测试必须遵守AIR原则。说明：单元测试在线上运行时，像空气（AIR）一样感觉不到，但在测试质量的保障方面，却是非常关键的。从宏观上说，好的单元测试具有自动化、独立性、可重复执行的特点。A：Automatic（自动化）；I：Independent（独立性）；R：Repeatable（可重复）。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>单元测试应该是全自动执行的，并且是非交互式的。测试用例通常是被定期执行的，执行过程必须完全自动化才有意义。需要人工检查输出结果的测试不是好的单元测试。单元测试中不准使用System.out进行人肉验证，必须使用assert验证。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>保持单元测试的独立性。为了保证单元测试稳定可靠且便于维护，单元测试用例之间决不能互相调用，也不能依赖执行的先后次序。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>反例：method2需要依赖method1的执行，将执行结果作为method2的输入。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>对于单元测试，要保证测试粒度足够小，有助于精确定位问题。单元测试粒度至多是类级别，一般是方法级别。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>说明：只有测试粒度足够小，才能在出错时尽快定位到出错位置。单元测试不负责检查跨类或者跨系统的交互逻辑，那是集成测试的领域。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>对于数据库相关的查询、更新、删除等操作，不能假设数据库里的数据是存在的，或者直接操作数据库把插入数据，请采用程序插入或者导入数据的方式准备数据。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>和数据库相关的单元测试，可以设定自动回滚机制，不给数据库造成脏数据。或者对单元测试产生的数据有明确的前后缀标识。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>为了更方便地进行单元测试，业务代码应避免出现以下情况。1）构造方法中做的事情过多。2）存在过多的全局变量和静态方法。3）存在过多的外部依赖。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>4）存在过多的条件语句。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>说明：多层条件语句建议使用卫语句、策略模式、状态模式等方式重构。</p>
</blockquote>
</blockquote>
<h2 id="◆-第4章-安全规约" tabindex="-1"><a class="header-anchor" href="#◆-第4章-安全规约" aria-hidden="true">#</a> ◆ 第4章 安全规约</h2>
<blockquote>
<blockquote>
<p>1【强制】隶属于用户个人的页面或者功能必须进行权限控制校验。说明：防止没有做水平权限校验就可以随意访问、修改、删除别人的数据，比如查看他人的私信内容。2【强制】用户敏感数据禁止直接展示，必须对展示数据进行脱敏。说明：个人手机号码显示为137****0969，隐藏中间4位，防止隐私泄露。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>3【强制】用户输入的SQL参数严格使用参数绑定或者METADATA字段值限定，防止SQL注入，禁止字符串拼接SQL访问数据库。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>4【强制】用户请求传入的任何参数必须做有效性验证。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>说明：忽略参数校验可能导致如下情况。1）page size过大导致内存溢出。2）恶意order by导致数据库慢查询。3）缓存击穿。4）SSRF。5）任意重定向。6）SQL注入、Shell注入和反序列化注入。7）正则输入源串拒绝服务ReDoS。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>Java代码用正则来验证客户端的输入，有些正则写法验证普通用户输入没有问题，但是如果攻击人员使用特殊构造的字符串来验证，则有可能导致死循环。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>5【强制】禁止向HTML页面输出未经安全过滤或未正确转义的用户数据。6【强制】在提交表单、AJAX时须执行CSRF安全验证。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>7【强制】URL外部重定向传入的目标地址必须执行白名单过滤。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>8【强制】在使用平台资源时，例如短信、邮件、电话、下单、支付，必须实现正确的防重放的机制，如数量限制、疲劳度控制、验证码校验，避免被滥刷导致用户受扰或平台资损。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>说明：例如，注册时将验证码发送到手机，如果没有限制次数和频率，那么可以利用此功能骚扰其他用户，并造成短信平台资源浪费。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>9【推荐】针对发帖、评论、发送即时消息等用户生成内容的场景，必须实行防刷、文本内容违禁词过滤等风控策略。</p>
</blockquote>
</blockquote>
<h3 id="◆-5-1-建表规约" tabindex="-1"><a class="header-anchor" href="#◆-5-1-建表规约" aria-hidden="true">#</a> ◆ 5.1 建表规约</h3>
<blockquote>
<blockquote>
<p>2【强制】表名、字段名必须使用小写字母或数字，禁止出现数字开头，禁止两个下画线中间只出现数字。数据库字段名的修改代价很大，因为无法进行预发布，所以字段名称需要慎重考虑。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>说明：MySQL在Windows系统中不区分大小写，但在Linux系统中默认区分大小写。因此，数据库名、表名和字段名，都不允许出现任何大写字母，避免节外生枝。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>[插图]</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>表名不使用复数名词。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>主键索引名为pk_字段名，唯一索引名为uk_字段名，普通索引名则为idx_字段名。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>6【强制】小数类型为decimal，禁止使用float和double类型。说明：在存储时，float和double类型存在精度损失的问题，很可能在比较值的时候，得到不正确的结果。如果存储的数据范围超过decimal的范围，那么建议将数据拆成整数和小数并分开存储。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>如果存储的字符串长度几乎相等，则使用char定长字符串类型。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>8【强制】varchar是可变长字符串，不预先分配存储空间，长度不要超过5000个字符，如果存储长度大于此值，则应定义字段类型为text，独立出来一张表，用主键来对应，避免影响其他字段的索引效率。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>9【强制】表必备三字段：id、create_time、update_time。说明：其中id必为主键，类型为bigint unsigned、单表时自增、步长为1。create_time和update_time的类型均为date_time。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>表的命名最好遵循“业务名称_表的作用”原则。[插图]</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>14【推荐】当单表行数超过500万或者单表容量超过2GB时，才推荐分库分表。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>说明：如果预计三年后的数据量无法达到这个级别，请不要在创建表时就分库分表。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>设置合适的字符存储长度，不但可以节约数据库表空间和索引存储，更重要的是能够提升检索速度。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>其中无符号值可以避免误存负数，且扩大了数据的表示范围。</p>
</blockquote>
</blockquote>
<h3 id="◆-5-2-索引规约" tabindex="-1"><a class="header-anchor" href="#◆-5-2-索引规约" aria-hidden="true">#</a> ◆ 5.2 索引规约</h3>
<blockquote>
<blockquote>
<p>1【强制】业务上具有唯一特性的字段，即使是多个字段的组合，也必须建成唯一索引。说明：不要以为唯一索引影响了insert速度，这个速度损耗可以忽略，但会明显提高查找速度；另外，即使在应用层做了非常完善的校验控制，只要没有唯一索引，根据墨菲定律，就必然有脏数据产生。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>2【强制】超过三个表禁止join。需要join的字段，数据类型必须绝对一致；当多表关联查询时，保证被关联的字段需要有索引。说明：即使双表join，也要注意表索引、SQL性能。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>3【强制】在varchar字段上建立索引时，必须指定索引长度，没必要对全字段建立索引，根据实际文本区分度决定索引长度即可。说明：索引的长度与区分度是一对矛盾体，一般对于字符串类型数据，长度为20的索引，区分度会高达90%以上，可以使用count(distinct left(列名, 索引长度))/count(*)的区分度来确定。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
<p>4【强制】页面搜索严禁左模糊或者全模糊，如果需要，那么请通过搜索引擎来解决。说明：索引文件具有B-Tree的最左前缀匹配特性，如果左边的值未确定，那么无法使用此索引。</p>
</blockquote>
</blockquote>
<blockquote>
<blockquote>
