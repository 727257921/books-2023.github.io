<p>内盘：主动卖出的资金</p>
<p>外盘：高于市场价格成交的股票数，主动买入的</p>
<p>通过内外盘的数据，可以大致看出当天买卖双方强弱的关系。</p>
<p>股价在下跌的过程中如果出现外盘大于内盘的时候，有可能是主力的货没出完，所以主力买入制造一种假象，为了吸引散户接盘</p>
<p>股价上涨中出现内盘大于外盘的情况，</p>
<p>内盘+外盘=成交量</p>
<p>没有成交量的个股就容易边缘化</p>
<p>怎么寻找强势股？判断标准就是量价齐升。上涨放量，下跌缩量，股价整体还是向上趋势，这就是一个总体震荡上行的，平稳上行，量价齐升，这种股票就很好。</p>
